const getResumeUrl = (doc) => {
  if (!doc?.file_path) return "#";

  if (
    doc.file_path.startsWith("http://") ||
    doc.file_path.startsWith("https://")
  ) {
    return doc.file_path;
  }

  return `${ASSET_BASE_URL}/${doc.file_path}`;
};

/**
 * getAtsResult — returns the ATS result object from any field name variant
 * the backend may use (ats_result, atsResult, ats, ats_report).
 */
const getAtsResult = (app) =>
  app.ats_result ?? app.atsResult ?? app.ats ?? app.ats_report ?? null;

/**
 * getAtsScore — resolves the numeric ATS score from all possible locations.
 * Priority: nested ats_result.overall_score → top-level ats_score → others.
 */
const getAtsScore = (app) => {
  const ats = getAtsResult(app);
  const score =
    ats?.overall_score ??
    ats?.ats_score ??
    ats?.score ??
    app?.ats_score ??
    app?.match_score ??
    app?.resume_score ??
    null;
  return score !== null && score !== undefined ? Math.round(getScore(score)) : null;
};

