const safeArray = (value) => {
  if (!value) return [];
  if (Array.isArray(value)) return value;
  if (typeof value === "string") {
    const trimmed = value.trim();
    if (trimmed.startsWith("[") && trimmed.endsWith("]")) {
      try {
        const parsed = JSON.parse(trimmed);
        if (Array.isArray(parsed)) return parsed;
      } catch (e) {}
    }
    return trimmed.split(",").map((s) => s.trim()).filter(Boolean);
  }
  return [];
};

const safeObject = (value) => {
  if (!value) return {};
  if (typeof value === "object" && !Array.isArray(value)) return value;
  if (typeof value === "string") {
    const trimmed = value.trim();
    if (trimmed.startsWith("{") && trimmed.endsWith("}")) {
      try {
        const parsed = JSON.parse(trimmed);
        if (typeof parsed === "object" && !Array.isArray(parsed)) return parsed;
      } catch (e) {}
    }
  }
  return {};
};

const getScore = (value) => {
  if (value === null || value === undefined) return 0;
  const num = Number(value);
  return isNaN(num) ? 0 : num;
};

const getGeneratedByLabel = (value) => {
  if (!value) return "In-house Scoring";
  const str = String(value).toLowerCase().trim();
  if (str === "hf_ai") return "Generated by Hugging Face AI";
  if (str === "in_house") return "Generated by In-house Scoring";
  if (str === "n8n_ai") return "AI Generated";
  if (str === "mixed") return "Mixed AI/In-house";
  return "In-house Scoring";
};

const dedupeArray = (list) => {
  const arr = safeArray(list);
  const seen = new Set();
  return arr.filter((item) => {
    if (!item) return false;
    const lower = String(item).toLowerCase().trim();
    if (seen.has(lower)) return false;
    seen.add(lower);
    return true;
  });
};

const parseList = (value) => dedupeArray(value);

const isValidUrl = (value) => {
  if (!value) return false;
  return value.startsWith("http://") || value.startsWith("https://");
};

const formatDateTime = (dateStr) => {
  if (!dateStr) return "—";
  try {
    return new Date(dateStr).toLocaleString("en-US", {
      month: "short",
      day: "numeric",
      year: "numeric",
      hour: "2-digit",
      minute: "2-digit",
    });
  } catch {
    return dateStr;
  }
};

